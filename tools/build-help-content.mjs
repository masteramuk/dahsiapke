import { readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const toolsDirectory = dirname(fileURLToPath(import.meta.url));
const projectDirectory = resolve(toolsDirectory, '..');
const documents = {
  helpme: {
    title: 'Quick Help',
    filename: 'HELPME.md',
    content: await readFile(resolve(projectDirectory, 'HELPME.md'), 'utf8')
  },
  usermanual: {
    title: 'Full User Manual',
    filename: 'USERMANUAL.md',
    content: await readFile(resolve(projectDirectory, 'USERMANUAL.md'), 'utf8')
  }
};

const output = `/* Copyright (c) 2026 haszeli.ahmad. All rights reserved. See LICENSE.md. */\n` +
  `/* Generated by tools/build-help-content.mjs. Do not edit manually. */\n` +
  `globalThis.DAHSIAPKE_HELP_DOCS = Object.freeze(${JSON.stringify(documents, null, 2)});\n`;

await writeFile(resolve(projectDirectory, 'help-content.js'), output, 'utf8');
console.log('Generated help-content.js from HELPME.md and USERMANUAL.md.');
